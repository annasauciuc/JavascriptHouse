// Create an object Score with the next properties

// ScoreID
// StudentID
// ActivityID
// Value
// ActivityName

function Activity(scoreID, studentID, activityID, value, activityName) {
    this.scoreID = scoreID;
    this.studentID = studentID;
    this.activityID = activityID;
    this.value = value;
    this.activityName = activityName;
}